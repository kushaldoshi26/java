import java.util.Scanner;

class ptn{
public static void main(String args[]){

Scanner sc = new Scanner(System.in);

	System.out.print("Enter the number for pattern :");
	int n = sc.nextInt();


	for(int i=1; 1<=n; i++){
	for(int j=1; j<=i; j++){
		System.out.print("*");	

	
	}
	System.out.print("");
	

}

}
}